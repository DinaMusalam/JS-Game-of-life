//function for button run/stop 
//at the first when the code is run the interval is null that's why the game is stopped at the first 
//when the use press the button then the interval is change to start. 
function game_toggle(game, force) {
  var interval = game.getInterval();
  if (force == "stop" || interval !== null) {
    // Stop
    clearInterval(interval);
    game.setTheInterval(null);
  } else {
    // start
    interval = setInterval(game.step, 50);// speed 50
    game.setTheInterval(interval);
  }
}
//Using jquery to recognize with button the user pressed to run the relevant action
function add_listeners(game, example_num) {
  $("#" + example_num + " .toggle, #" + example_num + " canvas").click(function(){
    game_toggle(game);
  });
  $("#" + example_num + " .step").click(function(){
    game_toggle(game, "stop");
    game.step();
  });
}

